<script>
    document.addEventListener("DOMContentLoaded", function() {
        // GALLERY_SLIDE
        var exGallerySlide = new Swiper(".exGallerySlide", {
            slidesPerView: 1,
            loop: true,
            spaceBetween: 0,
            autoplay: {
                delay: 5000,
                disableOnInteraction: true,
            },
            autoplay: false,
            pagination: {
                el: ".swiper-pagination",
                clickable: true,
            },
            navigation: {
                nextEl: ".swiper-button-next",
                prevEl: ".swiper-button-prev"
            },
        });
        // GALLERY_SLIDE
        var gallerySlide = new Swiper(".gallerySlide", {
            slidesPerView: 5,
            loop: true,
            spaceBetween: 0,
            autoplay: {
                delay: 1000,
                disableOnInteraction: true,
            },
        });
        // GATEWAY_SLIDE
        var gatewaySlide;

        function initSwiper() {
            if (window.innerWidth <= 768) {
                // Initialize Swiper if it hasn't been initialized yet
                if (!gatewaySlide) {
                    gatewaySlide = new Swiper(".gatewaySlide", {
                        slidesPerView: 2.5,
                        loop: $('.gatewaySlide .swiper-slide').length > 1,
                        spaceBetween: 10,
                        watchSlidesProgress: true,
                        centeredSlides: true,
                        roundLengths: true,
                        lazy: {
                            loadPrevNext: true,
                        },
                        autoplay: {
                            delay: 2500,
                            disableOnInteraction: false,
                        },
                        pagination: {
                            el: ".swiper-pagination",
                            clickable: true,
                        },
                        breakpoints: {
                            576: {
                                slidesPerView: 3,
                                loop: $('.gatewaySlide .swiper-slide').length > 2,
                            },
                            768: {
                                slidesPerView: 3,
                                loop: $('.gatewaySlide .swiper-slide').length > 3,
                            }
                        }
                    });
                }
            } else {
                // Destroy Swiper if it has been initialized
                if (gatewaySlide) {
                    gatewaySlide.destroy(true, true);
                    gatewaySlide = null;
                }
            }
        }

        // Initialize Swiper when the page loads
        initSwiper();

        // Add resize event listener to check window size on resize
        window.addEventListener('resize', initSwiper);


    });
</script>

